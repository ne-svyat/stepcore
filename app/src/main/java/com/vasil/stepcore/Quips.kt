package com.vasil.stepcore

/**
 * Реплики приложения («мысли StepCore»).
 *
 * АРХИТЕКТУРА. Контекст -> классификаторы -> корзины -> движок выбора.
 * Классификатор не знает про UI, движок не знает про Android, показ не
 * знает про правила. Новый классификатор (темп, серия дней, время суток,
 * погода) добавляется одной строкой в список CLASSIFIERS и своей корзиной
 * реплик - ни движок, ни экран при этом не меняются.
 *
 * ПОВТОРЫ. Реплики выдаются «мешком»: пул перемешивается и выдаётся до
 * конца, и лишь затем тасуется заново. Поэтому одна и та же строка не
 * приходит дважды подряд и не выпадает часто, пока не показаны остальные.
 * При смене корзины мешок пересобирается - реплики следуют за активностью.
 *
 * Всего строк: 267.
 */

/** Снимок ситуации. Поля добавляются свободно - классификаторы берут нужное. */
data class QuipContext(
    val steps: Int,
    val runSteps: Int = 0,
    val hourOfDay: Int = 12,
)

/** Правило: по контексту возвращает ярлык корзины или null, если неприменимо. */
interface QuipClassifier {
    fun bucket(ctx: QuipContext): String?
}

/** Первый классификатор: сколько пройдено за сегодня. */
object StepsClassifier : QuipClassifier {
    override fun bucket(ctx: QuipContext): String = when {
        ctx.steps < 7_000 -> "low"
        ctx.steps < 12_000 -> "mid"
        ctx.steps < 20_000 -> "good"
        ctx.steps < 30_000 -> "high"
        else -> "epic"
    }
}

