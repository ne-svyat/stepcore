    private val POOLS: Map<String, List<String>> = mapOf(
        "low" to low, "mid" to mid, "good" to good, "high" to high, "epic" to epic,
    )

    /** Общий пул подмешивается всегда: такие реплики уместны в любой день. */
    private const val COMMON_KEY = "common"

    private var bagKey = ""
    private val bag = ArrayDeque<String>()
    private var last: String? = null

    /** Следующая реплика для контекста. Повторов подряд не бывает. */
    fun next(ctx: QuipContext): String {
        val keys = CLASSIFIERS.mapNotNull { it.bucket(ctx) }
        val key = keys.joinToString("+")
        if (key != bagKey || bag.isEmpty()) refill(key, keys)
        var pick = bag.removeFirstOrNull() ?: return last ?: common.first()
        if (pick == last && bag.isNotEmpty()) {
            val alt = bag.removeFirst()
            bag.addLast(pick)
            pick = alt
        }
        last = pick
        return pick
    }

    private fun refill(key: String, keys: List<String>) {
        bagKey = key
        val items = ArrayList<String>()
        for (k in keys) items.addAll(POOLS[k].orEmpty())
        items.addAll(common)
        items.shuffle()
        bag.clear()
        bag.addAll(items)
    }

    /** Для проверок: сколько всего строк знает движок. */
    fun size(): Int = POOLS.values.sumOf { it.size } + common.size
}
