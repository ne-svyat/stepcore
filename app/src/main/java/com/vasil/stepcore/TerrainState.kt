package com.vasil.stepcore

import kotlinx.coroutines.flow.MutableStateFlow

/**
 * Ручная метка местности — Сегмент 1 адаптивного слоя (бывш. MotionLab).
 *
 * Уклон — это физический параметр активности того же класса, что вес и груз:
 * он реально меняет энергозатраты (кривая Минетти). Сейчас задаётся вручную
 * кнопкой; позже сможет выставляться обученным агентом. Ядро лишь читает это
 * состояние и НЕ знает, кто его записал — тот же паттерн, что профиль
 * калибровки (дефолт ИЛИ откалибровано).
 *
 * Значение НЕ персистится намеренно: уклон транзиентен (ты на склоне СЕЙЧАС).
 * После перезапуска процесса — FLAT (безопасный дефолт, нулевая поправка).
 * Это исключает баг «залипшего UP» после закрытия приложения.
 *
 * Сегмент 1 только фиксирует метку в журнал. Влияние на ккал — Сегмент 2.
 */
object TerrainState {
    enum class Incline { UP, FLAT, DOWN }
    val incline = MutableStateFlow(Incline.FLAT)
}
