package com.vasil.stepcore.survival.engine

/**
 * Следы на снегу.
 *
 * Первый слой мира, который не является погодой. Он намеренно построен как
 * СЛЕДСТВИЕ земли, а не как отдельная случайность поверх неё:
 *
 *  - след невозможен без покрова: голая земля не держит отпечаток;
 *  - след невозможен, пока идёт снег или метёт: его заметает на глазах;
 *  - возраст следа = возраст снега. По свежему покрову след может быть
 *    только свежим — это и есть та самая «чистая страница», о которой
 *    говорит строка «на свежем снегу ни одной отметины».
 *
 * Зверя как объекта тут ещё нет: есть только его отпечаток. Это осознанно.
 * Сначала мир должен научиться ХРАНИТЬ информацию (снег как носитель),
 * и только потом в него можно селить того, кто эту информацию оставляет.
 * Обратный порядок дал бы зверей, телепортирующихся по бесснежной тайге.
 *
 * Детерминизм: вся случайность берётся из rng тика. Тот же день мира —
 * тот же след.
 */
object TrackModel {

    /** Минимальный покров, на котором отпечаток вообще читается. */
    const val MIN_SNOW_CM = 2

    /**
     * Базовая вероятность встретить след в подходящий день.
     * 0.28 подобрано прогоном: примерно один след в 4-5 дней с покровом —
     * достаточно, чтобы тайга казалась обитаемой, и достаточно редко,
     * чтобы встреча со следом оставалась событием.
     */
    private const val P_BASE = 0.28

    data class Track(val species: String, val freshDays: Int)

    /**
     * Читается ли сегодня чей-нибудь след.
     *
     * ВАЖНО: rng черпается ВСЕГДА, независимо от условий, — иначе порядок
     * обращений к случайности зависел бы от погоды, и один и тот же тик
     * давал бы разные тексты при пересчёте мира кусками.
     */
    fun roll(rng: SplitMix64, st: WeatherState, season: Int): Track? {
        val draw = rng.nextDouble()
        val pick = rng.nextDouble()

        // Снег не держит след / след заметает прямо сейчас.
        if (st.snowCm < MIN_SNOW_CM) return null
        if (st.precip >= 3) return null      // идёт снег: пишет поверх
        if (st.wind == 3) return null        // буря: стирает всё
        // Снег лёг сегодня — по нему ещё никто не успел пройти.
        if (st.snowAgeDays < 1) return null

        // Сильный ветер портит старые следы: остаются только вчерашние.
        if (st.wind == 2 && st.snowAgeDays > 2) return null

        if (draw >= P_BASE) return null

        val species = pickSpecies(pick, st, season)
        return Track(species, st.snowAgeDays)
    }

    /**
     * Кто прошёл. Веса зависят от того, ЧТО сейчас в тайге:
     *
     *  - медведь ходит только пока не залёг: ранний снег, ещё не мороз,
     *    и весной, когда снег уже оседает. В январе медвежьего следа
     *    не бывает — это была бы ложь, которую видно любому охотнику;
     *  - по глубокому снегу мелкий зверь ходит меньше, а лось — по-прежнему:
     *    его ноги для этого и сделаны;
     *  - волк зимой держится стаей и чаще попадается на глаза следом,
     *    чем сам.
     */
    private fun pickSpecies(draw: Double, st: WeatherState, season: Int): String {
        val deep = st.snowCm >= 40
        // Медведь ложится по холоду и снегу, а встаёт по теплу — не по дате.
        // Весенний след при -10 был бы ровно той ложью, ради которой
        // и заведён физический слой: календарь говорит «весна», зверь спит.
        val bearAwake = (season == 3 && st.tempC > -8 && st.snowCm < 20) ||
            (season == 1 && st.tempC > -5 && st.snowCm < 30)

        val names = ArrayList<String>()
        val weights = ArrayList<Double>()
        fun add(name: String, w: Double) { if (w > 0.0) { names.add(name); weights.add(w) } }

        add("hare", if (deep) 22.0 else 30.0)      // заяц — самый частый след
        add("fox", if (deep) 10.0 else 18.0)       // лиса мышкует по мелкому снегу
        add("moose", if (deep) 20.0 else 12.0)     // лосю глубина не помеха
        add("sable", if (deep) 8.0 else 11.0)      // соболь
        add("wolf", 8.0)                            // волки — всегда цепочкой
        add("lynx", 5.0)                            // рысь
        add("wolverine", 4.0)                       // росомаха
        add("grouse", if (deep) 8.0 else 6.0)       // куропатка/глухарь: наброды
        add("bear", if (bearAwake) 5.0 else 0.0)
        add("unknown", 6.0)                         // не разобрал

        val total = weights.sum()
        var acc = 0.0
        val target = draw * total
        for (i in names.indices) {
            acc += weights[i]
            if (target < acc) return names[i]
        }
        return names.last()
    }
}
