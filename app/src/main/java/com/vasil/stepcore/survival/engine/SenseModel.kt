package com.vasil.stepcore.survival.engine

import kotlin.math.max
import kotlin.math.min

/**
 * ГРАНИЦА ЗНАНИЯ. Докуда человек сегодня ВИДИТ и докуда СЛЫШИТ.
 *
 * Радара в тайге нет. Есть человек у костра и карта, которую он чертит по
 * памяти. Всё, что попадает на эту карту, он узнал одним из трёх способов:
 * увидел, услышал, прочёл след. Больше взяться неоткуда.
 *
 * Поэтому «прибор сбоит в непогоду» — неверная мысль. Верная: в непогоду
 * ЧЕЛОВЕК УЗНАЁТ МЕНЬШЕ. Экран не шумит — экран пустеет. Разница
 * принципиальная: ложная метка в игре про честность убила бы доверие к
 * экрану навсегда, а честное «я сегодня слеп» — это игровая ситуация.
 *
 * ЗЕРКАЛО ЗАПАХА. Запах уносит ОТ тебя: зверь под ветром знает про лагерь.
 * Звук приносит К тебе: слышно лучше то, что стоит ПРОТИВ ветра, то есть с
 * той стороны, откуда дует. Одна физика, две стороны — и потому конус
 * запаха и лепесток слуха смотрят в противоположные стороны.
 *
 * ЛАНДШАФТ (v119). Все функции принимают openness — насколько открыт сектор:
 * 1.0 — обычная тайга, больше — марь, гарь, лёд реки, вершина сопки; меньше
 * — густой ельник, распадок. Пока всегда 1.0. Слой ляжет сюда одним числом,
 * не трогая ни одну формулу.
 */
object SenseModel {

    /** Ясный день в тайге: дальше этого человек зверя не разглядит всё равно. */
    private const val SIGHT_BASE_KM = 1.5

    /** Тихий день: обычная дальность слышимости зверя. */
    private const val HEAR_BASE_KM = 4.0

    /**
     * Докуда видно СЕГОДНЯ.
     *
     * Порядок множителей — от самого сильного к самому слабому:
     * метель отнимает почти всё, туман — почти всё, короткий день — половину,
     * полная луна в ясную ночь немного возвращает.
     */
    fun sightKm(
        cloud: Int, precip: Int, fog: Boolean, wind: Int,
        lightX10: Int, moon: Int, openness: Double = 1.0,
    ): Double {
        var r = SIGHT_BASE_KM * openness

        if (fog) r *= 0.20                       // в тумане не видно и своих рук
        r *= when (precip) {
            0 -> 1.0
            1 -> 0.75                            // морось
            2 -> 0.55                            // дождь
            3 -> 0.50                            // снегопад
            else -> 0.18                         // ливень, сильный снег
        }
        if (wind >= 3) r *= 0.40                 // буря: снег и сор в лицо
        r *= when (cloud) {
            0 -> 1.0
            1 -> 0.95
            else -> 0.85                         // хмарь съедает контраст
        }
        // Короткий день. Полярной ночи в тайге нет, но пять часов света —
        // это пять часов; остальное — сумерки, в которых не видно ничего.
        r *= when {
            lightX10 <= 60 -> 0.35
            lightX10 <= 90 -> 0.60
            lightX10 <= 120 -> 0.85
            else -> 1.0
        }
        // Ясная ночь при полной луне по снегу — почти день.
        if (cloud == 0 && moon in 3..5) r *= 1.15

        return min(2.5, max(0.08, r))
    }

    /**
     * Докуда слышно СЕГОДНЯ — без учёта стороны.
     *
     * Ветер — главный враг слуха: он не столько уносит звук, сколько гудит
     * в вершинах и забивает всё остальное. Мороз, наоборот, звук несёт:
     * в минус двадцать пять слышно, как за версту трещит дерево.
     */
    fun hearKm(
        wind: Int, precip: Int, tempC: Int, fog: Boolean, openness: Double = 1.0,
    ): Double {
        var r = HEAR_BASE_KM * openness

        r *= when (wind) {
            0 -> 1.25                            // мёртвая тишина
            1 -> 1.0
            2 -> 0.65
            else -> 0.30                         // буря глушит всё
        }
        r *= when (precip) {
            0 -> 1.0
            1, 2 -> 0.85                         // шум дождя
            3 -> 0.80
            else -> 0.60
        }
        if (tempC <= -20) r *= 1.20              // морозный воздух звонкий
        if (fog) r *= 1.05                       // туман звук скорее несёт

        return min(6.5, max(0.30, r))
    }

    /**
     * Слышимость В КОНКРЕТНУЮ СТОРОНУ. Лепесток, а не круг.
     *
     * С наветренной стороны (оттуда, откуда дует) звук приносит к тебе;
     * с подветренной — уносит прочь. Это ровно зеркало конуса запаха.
     */
    fun hearKmIn(baseKm: Double, sector: Int, windDir: Int, wind: Int): Double {
        if (wind == 0) return baseKm             // штиль: круг
        val d = Compass.diff(sector, windDir)
        return baseKm * when (d) {
            0, 1 -> 1.45                         // против ветра — слышно далеко
            2 -> 1.0
            else -> 0.55                         // по ветру — почти не слышно
        }
    }

    /**
     * Насколько человек ошибётся с румбом того, что УСЛЫШАЛ.
     *
     * Ноль — направление уверенное. Единица — «где-то там». Двойка — «эхо
     * множит, направление не угадать»: туман, снегопад, гул ветра.
     *
     * Радар не врёт метками. Он расширяет ореол — то есть признаётся, что
     * не уверен. Это единственная честная форма «помех».
     */
    fun bearingErrHeard(fog: Boolean, precip: Int, wind: Int): Int = when {
        fog || precip >= 3 || wind >= 3 -> 2
        wind >= 2 || precip >= 1 -> 1
        else -> 1
    }

    /** Тихий морозный штиль — единственные условия, когда слух точен. */
    fun bearingErrHeardCalm(wind: Int, precip: Int, fog: Boolean): Int =
        if (wind == 0 && precip == 0 && !fog) 0 else bearingErrHeard(fog, precip, wind)
}
