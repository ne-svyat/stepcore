package com.vasil.stepcore.survival.engine

import kotlin.math.abs

/**
 * Румбы. Восемь направлений — ровно столько, сколько человек различает
 * без прибора, и ровно столько, сколько нужно запаху.
 */
object Compass {
    val RU = arrayOf("С", "СВ", "В", "ЮВ", "Ю", "ЮЗ", "З", "СЗ")

    /** Куда дует ветер, дующий ИЗ dir (метеорологическое соглашение). */
    fun downwind(dir: Int): Int = (dir + 4) % 8

    /** Угловое расстояние между румбами в шагах по 45 градусов (0..4). */
    fun diff(a: Int, b: Int): Int {
        val d = abs(a - b) % 8
        return minOf(d, 8 - d)
    }
}

/**
 * Направление ветра.
 *
 * ЖИВЁТ В ОТДЕЛЬНОМ ПОТОКЕ СЛУЧАЙНОСТИ. Это принципиально: если бросать
 * направление из общего потока тика, сдвинется вся последующая погода,
 * и каждая новая механика ломала бы миры всех существующих экспедиций.
 * Соль на seed даёт независимую струю: погодный поток теперь заморожен
 * навсегда, сколько бы слоёв мы ни добавили сверху.
 *
 * Ветер инерционен: он не разворачивается на 180 градусов за ночь.
 * Зато прохождение фронта его поворачивает — как и положено: за холодным
 * фронтом ветер заходит к северо-западу и северу.
 */
object WindField {

    const val SALT = 0x57A1_7A0F_1D9E_2B45L
    private const val NORTHWEST = 7

    fun initial(seed: Long): Int = SplitMix64.forTick(seed xor SALT, 0).nextInt(8)

    /**
     * Направление следующего дня.
     * @param front фронт из погодной модели: 3 = тыл, ветер заходит к северу
     */
    fun step(seed: Long, tick: Int, prev: Int, front: Int): Int {
        val rng = SplitMix64.forTick(seed xor SALT, tick)
        val d = rng.nextDouble()

        // За фронтом ветер разворачивает решительно и в известную сторону.
        if (front == 3 && d < 0.55) {
            val target = if (rng.nextDouble() < 0.5) NORTHWEST else 0 // СЗ или С
            return turnTowards(prev, target)
        }

        return when {
            d < 0.55 -> prev                                   // держится
            d < 0.80 -> (prev + 1) % 8                         // отходит по часовой
            d < 0.95 -> (prev + 7) % 8                         // против часовой
            else -> (prev + 2 + rng.nextInt(5)) % 8            // резкая смена
        }
    }

    /** Поворот на один румб в сторону цели — ветер не прыгает. */
    private fun turnTowards(from: Int, target: Int): Int {
        if (from == target) return from
        val cw = (target - from + 8) % 8
        return if (cw <= 4) (from + 1) % 8 else (from + 7) % 8
    }
}

/**
 * Запах.
 *
 * Это то, что превращает погоду из декорации в механику. Не «ветрено —
 * неприятно», а «ветер повернул — и ночью к лабазу пришли по запаху».
 *
 * Главная правда тайги, которую модель обязана воспроизводить:
 * ЗВЕРЬ НЕ ЧУЕТ ПРОТИВ ВЕТРА. Волк в трёхстах метрах с наветренной стороны
 * не знает о лагере ничего. Он же в пяти километрах ниже по ветру знает
 * всё: что тут человек, что горит костёр и что в лабазе висит мясо.
 *
 * Поэтому запах — это не «шанс встречи», а конус: сектор + дальность.
 */
object ScentModel {

    /** Базовый радиус запаха лагеря в идеальных условиях, км. */
    private const val BASE_KM = 3.0

    /**
     * Дальность запаха лагеря сегодня.
     *
     * Штиль запах не разносит — он стоит лужей вокруг костра. Сильный ветер
     * несёт далеко. Буря рвёт струю в клочья: далеко, но обрывками.
     * Мороз связывает летучие вещества — в тридцатиградусный мороз тайга
     * почти не пахнет. Дождь смывает запах, снегопад глушит. Туман и
     * инверсия держат его низко и далеко.
     */
    fun campScentKm(st: WeatherState): Double {
        var r = BASE_KM
        r *= when (st.wind) {
            0 -> 0.6
            1 -> 1.0
            2 -> 1.45
            else -> 0.75
        }
        r *= when {
            st.tempC <= -20 -> 0.5
            st.tempC <= -5 -> 0.75
            st.tempC >= 10 -> 1.3
            else -> 1.0
        }
        r *= when (st.precip) {
            1, 2 -> 0.35   // дождь смывает
            3, 4 -> 0.6    // снегопад глушит
            else -> 1.0
        }
        if (st.fog) r *= 1.3
        return r
    }

    /**
     * Чует ли зверь лагерь.
     *
     * @param nose чутьё зверя: медведь 1.6, волк 1.5, росомаха 1.4,
     *             лиса 1.0, лось 0.7, рысь 0.5 (охотится глазами)
     */
    fun smells(distKm: Double, sector: Int, st: WeatherState, windDir: Int, nose: Double): Boolean {
        val reach = campScentKm(st) * nose
        // Штиль: сектора нет, запах расходится кругом, но недалеко.
        if (st.wind == 0) return distKm <= reach * 0.5
        // Зверь должен стоять ТАМ, КУДА уносит запах.
        if (Compass.diff(sector, Compass.downwind(windDir)) > 1) return false
        return distKm <= reach
    }
}
